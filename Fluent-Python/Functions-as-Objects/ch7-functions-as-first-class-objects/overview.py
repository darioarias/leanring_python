# First class objects are those that can be;
## Created at runtime
## Assigned to a variable or element in a data structure
## Passed as an argument to a function
## Returned as the result of a function

# some examples include;
# Functions
# Integers
# Strings
# Dictionaries

# The term “first-class functions” is widely used as shorthand 
# for “functions as first-class objects.” 
# It’s not ideal because it implies an “elite” among functions. 
# In Python, all functions are first-class.